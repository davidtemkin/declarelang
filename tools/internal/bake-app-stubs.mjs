// tools/internal/bake-app-stubs.mjs — generate the committed cold-stub index.html
// for every program directory under apps/.
//
// WHY THESE EXIST (the one gap they fill). The directory-program rule — `…/name/` ≡
// `…/name/name.declare` — is answered dynamically by both hosts: the dev server
// applies it before any static serving (server/create.mjs, so these files are
// SHADOWED under dev and never served there), and the static-host service worker
// applies the same shared rule once installed (service-worker.js). The one requester
// neither can reach is a COLD visitor on a dumb static host — first navigation, no
// worker yet. GitHub Pages answers that request with a 404; any host that serves
// `index.html` for a directory answers it with this file. The stub is therefore the
// lowest-common-denominator fix: no rewrites, no 404 tricks, no host configuration —
// one hierarchy that behaves identically on GitHub Pages, Firebase, S3, nginx, and
// under the dev server (where it is invisible).
//
// WHAT A STUB IS. browser/serve-core.js's stubPage(): the same shell as the run page
// both hosts serve, with `main` computed at runtime by the shared directoryProgram
// rule (deploy-prefix- and slash-form-agnostic), and one cold-only behavior: a
// request modifier (?viewer, ?extract, …) can't be answered without the worker, so
// the stub boots the RUN view and reloads once the worker is ready — the reload is
// SW-served in the requested mode. Parity with the SW's page is by construction:
// both are serve-core output.
//
// V1 RULING — no baked crawler content. The root index.html remains the one curated
// SEO surface (bake-homepage-crawler.mjs); baking ?extract into every stub would
// compile every app on every commit for little gain. stubPage's shell keeps the
// staticBlock seam open should that change.
//
// LIFECYCLE. Rides the pre-commit hook BEFORE stamp-version.mjs, like every committed
// artifact: this bakes the bytes; stamp-version stamps the `?v=` cache-buster and
// folds the (normalized) stub bytes into the BUILD_ID. Regeneration preserves an
// existing stamp so the two tools never churn each other. Idempotent: unchanged
// content is not rewritten.
//
//   node tools/internal/bake-app-stubs.mjs

import path from "node:path";
import { readFileSync, writeFileSync, readdirSync, existsSync, statSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { stubPage } from "../../browser/serve-core.js";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "../..");
const APPS = path.join(ROOT, "apps");
const MARKER = "<!-- GENERATED by tools/internal/bake-app-stubs.mjs — do not edit; edit browser/serve-core.js stubPage() and regenerate -->\n";
const V_RE = /((?:\.{1,2}\/)+bundles\/declare-boot\.js)(\?v=[^"']*)?/;

/** Program directories: every apps/<name>/ holding a name-matched <name>.declare. */
export function stubDirs(root = ROOT) {
  const apps = path.join(root, "apps");
  if (!existsSync(apps)) return [];
  return readdirSync(apps).sort().filter((n) => {
    const d = path.join(apps, n);
    return statSync(d).isDirectory() && existsSync(path.join(d, n + ".declare"));
  }).map((n) => path.join(apps, n));
}

let wrote = 0, kept = 0;
for (const dir of stubDirs()) {
  const name = path.basename(dir);
  const out = path.join(dir, "index.html");
  // Preserve the stamped cache-buster across regeneration (stamp-version owns it).
  const prior = existsSync(out) ? readFileSync(out, "utf8") : "";
  const v = (prior.match(V_RE) || [])[2] ?? "?v=";
  // A `demos/` dir means live-edit panels seeded from demos/<name>.declare. The page
  // NAMES them in boot() (the curated root page does), but the SW's browse-to-run
  // wrapper for a bare `<name>.declare` URL can't read the filesystem to know them —
  // so commit a `demos.json` beside the program that boot falls back to (boot-uniform).
  // EXCEPTION: the docs app names none — its ~50 inline-example editors seed ON
  // DEMAND from the doc model as the reader scrolls (boot-uniform), never up front —
  // so it gets no demos.json (pre-seeding all of them would be a load-time regression).
  const demoDir = path.join(dir, "demos");
  if (existsSync(demoDir) && name !== "docs") {
    const demos = readdirSync(demoDir).filter((f) => f.endsWith(".declare")).map((f) => f.replace(/\.declare$/, "")).sort();
    const dj = path.join(dir, "demos.json");
    const djText = JSON.stringify(demos) + "\n";
    if (!(existsSync(dj) && readFileSync(dj, "utf8") === djText)) { writeFileSync(dj, djText); wrote++; }
  }
  // apps/<name>/ is always two levels below the tree root the platform deploys at.
  const html = MARKER + stubPage({
    name,
    bootUrl: `../../bundles/declare-boot.js${v}`,
    serveCoreUrl: "../../browser/serve-core.js",
    iconBase: "../../assets/",
  });
  if (prior === html) { kept++; continue; }
  writeFileSync(out, html);
  wrote++;
}
console.log(`bake-app-stubs: ${wrote} written, ${kept} unchanged`);
